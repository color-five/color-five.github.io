% 正态分布3条曲线对比（无报错版）
clear; clc; close all;

% 1. 定义x轴
x = -5:0.01:15;

% 2. 直接用公式计算正态分布（不用任何工具箱）
y1 = (1/sqrt(2*pi*1)) * exp(-(x-0).^2/(2*1));   % N(0,1)
y2 = (1/sqrt(2*pi*4)) * exp(-(x-0).^2/(2*4));   % N(0,4)
y3 = (1/sqrt(2*pi*1)) * exp(-(x-10).^2/(2*1));  % N(10,1)

% 3. 绘图
plot(x, y1, 'r-', 'LineWidth',2); hold on;
plot(x, y2, 'b--','LineWidth',2);
plot(x, y3, 'g-.','LineWidth',2);
hold off;

% 4. 标注
title('N(0,1), N(0,4), N(10,1) 密度曲线对比');
xlabel('x');
ylabel('f(x)');
legend('N(0,1)','N(0,4)','N(10,1)');
grid on;

% 5. 差异说明
fprintf('==================================\n');
fprintf('曲线差异说明（作业直接写）：\n');
fprintf('1. N(0,1)：中心在0，尖窄\n');
fprintf('2. N(0,4)：中心在0，矮宽（方差大）\n');
fprintf('3. N(10,1)：中心在10，形状同N(0,1)\n');
fprintf('==================================\n');