%题目矩阵应该给错了吧，这里我自己修正，全为3x3的矩阵
clear; clc;% 清空环境

%% 1. 生成正确维度的子矩阵
E = eye(3);        % 3×3 单位矩阵
R = rand(3,3);     %  3×3随机矩阵
O = zeros(3,3);    %  3×3零矩阵
S = diag([1 2 3]); % 3×3 对角矩阵

%% 2. 构造分块矩阵 A
A = [E  R; O  S];

%% 3. 直接计算 A²
A2_calc = A^2;

%% 4. 按分块公式构造理论结果
E_RS = E ;    % 左上角️
RS2  = R + R*S;  % 右上角
O_mat= zeros(3,3); % 左下角
S2   = S^2;        % 右下角

A2_theory = [E_RS  RS2; O_mat  S2];

%% 5. 验证是否相等（输出误差，接近0即正确）
disp('矩阵 A² 与理论公式的最大误差：');
max_error = max(max(abs(A2_calc - A2_theory)))

%% 6. 显示结果
disp('-----------------------');
disp('直接计算的 A²：');
disp(A2_calc);

disp('-----------------------');
disp('公式计算的 A²：');
disp(A2_theory);